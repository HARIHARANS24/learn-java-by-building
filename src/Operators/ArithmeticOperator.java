package Operators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        int z;
        int w;
        int r;
        int s;
        int t;

        z = x+y;
        w = x-y;
        r = x*y;
        s = x%y;
        t = x/y;

        System.out.println(z);
        System.out.println(w);
        System.out.println(r);
        System.out.println(s);
        System.out.println(t);
    }
}
