package Operators;

public class IncrementandDecrementOperator {
    public static void main(String[] args) {

        int x = 1;
        x ++;
        System.out.println(x);
        x += 1;
        System.out.println(x);
        x ++;
        System.out.println(x);

        System.out.print("\n");

        int y = 1;
        y --;
        System.out.println(y);
        y -= 1;
        System.out.println(y);
        y --;
        System.out.println(y);
    }
}
